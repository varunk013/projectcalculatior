public class MathFunctions {

    double squre(float num1 ){
        return num1*num1;
    }
    double cube(float num1){
        return num1*num1*num1;
    }
    double module(float num1,float num2){
        return num1 % num2;
    }
    double power(float num1,float num2){
        return Math.pow(num1, num2);
    }
}
